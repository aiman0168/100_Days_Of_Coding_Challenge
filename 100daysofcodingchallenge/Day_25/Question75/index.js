//Compound Assignment Operators: Use compound assignment operators to simplify arithmetic operations. Start with x = 4 and perform a series of operations (addition, subtraction, multiplication, division) on x using compound operators.
function compoundOperators() {
    var x = 4; // Initial value
    console.log("Initial value:", x);
    //Addition
    x += 2;
    console.log("Addition value:", x);
    //Subtraction
    x -= 2;
    console.log("Subtraction Value:", x);
    //Multiplication
    x *= 2;
    console.log("Multiplied Value:", x);
    //Division
    x /= 2;
    console.log("divided Value:", x);
}
compoundOperators();
